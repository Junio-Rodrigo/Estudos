let b = 3

b += 2        // equivalente a b = b + 2
console.log(b) 

b -= 2        // equivalente a b = b - 2
console.log(b) 

b *= 2        // equivalente a b = b * 2
console.log(b)

b /= 2        // equivalente a b = b /2
console.log(b)

b %= 2       // equivalente a b = b % 2 (o resultado são os restos de uma divisão) 
console.log(b)

b **= 2      // equivalente a b = b ** 2
console.log(b)
